// <http://merthsoft.com/linkguide/ti83+/fformat.html>
#define FILE_HEADER_SIZE 55
#define VARIABLE_HEADER_SIZE 17
#define Y_VARIABLE_HEADER_SIZE 2

int main(int argc, const char *argv[]) {
    int rv;
    assert(argc >= 2);
    const char *filename = argv[1];
    FILE *const f = fopen(filename, "r");
    assert(f != NULL);

    rv = fseek(f, FILE_HEADER_SIZE, SEEK_CUR);
    assert(!rv);

    rv = fseek(f, VARIABLE_HEADER_SIZE, SEEK_CUR);
    assert(!rv);

    rv = fseek(f, Y_VARIABLE_HEADER_SIZE, SEEK_CUR);
    assert(!rv);

    const char *const *alternate_translations = NULL;

    while (!feof(f)) {
        uint8_t buf[128];
        const size_t n = fread(buf, sizeof (uint8_t), 128, f);
        assert(n > 0);

        for (size_t i = 0; i < n; i++) {
            const uint8_t token = buf[i];
            const char *translation = NULL;

            if (alternate_translations != NULL) {
                translation = alternate_translations[token];
                alternate_translations = NULL;
            } else if (alternate_translation_sets[token] != NULL) {
                alternate_translations = alternate_translation_sets[token];
            } else {
                translation = translations[token];
            }

            if (translation) {
                fputs(translation, stdout);
            }
        }
    }

    fputs("\n", stdout);
    fclose(f);

    return 0;
}
